#include <iostream>
#include<cmath>
using namespace std;
int main(){
   int r=6,h=8;
   const double pi =3.14;
   double volumeofcylinder;
   volumeofcylinder = pi*r*r*h;
   cout<< "Radius of cylinder is : "<<r<<endl;
   cout<< "Height of cylinder is : "<<h<<endl;
   cout<< "Volume of cylinder is : "<<volumeofcylinder;
return 0;
}

